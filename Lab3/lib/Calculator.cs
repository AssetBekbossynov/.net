using System;

namespace lib
{
    public class Calculator
    {
        public static int Substract(int a, int b){
            return a-b;
        }        
        public static int Add(int a, int b){
            return a+b;
        }        
        public static int Multiply(int a, int b){
            return a*b;
        }        
        public static double Divide(int a, int b){
            return a/b;
        }        
    }
}
